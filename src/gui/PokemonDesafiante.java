package gui;

public class PokemonDesafiante {
	  String nome;
	  int Ata;
	  int Vida;
	  
	  
	  
	  public void DefinirPokemonDesafiante( String Poke)
	  
	  {
		  nome = Poke;
		  if (nome.equals("Miau"))
		  {
			  nome = "Miau";
		      Ata = 35;
		      Vida = 100;
		      
		  }   
      }
	  
}
